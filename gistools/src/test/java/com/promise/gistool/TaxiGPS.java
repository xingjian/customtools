public class TaxiGPS {
    public String id;
    public String positionti;
    public String company;
    public String x;
    public String y;
    public String speed;
    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getPositionti() {
        return positionti;
    }
    public void setPositionti(String positionti) {
        this.positionti = positionti;
    }
    public String getCompany() {
        return company;
    }
    public void setCompany(String company) {
        this.company = company;
    }
    public String getX() {
        return x;
    }
    public void setX(String x) {
        this.x = x;
    }
    public String getY() {
        return y;
    }
    public void setY(String y) {
        this.y = y;
    }
    public String getSpeed() {
        return speed;
    }
    public void setSpeed(String speed) {
        this.speed = speed;
    }
    
}
