package com.promise.gistool;

public class ETCPPark {
  private int index;
  private String x;
  private String y;
  private String wkt;
public int getIndex() {
    return index;
}
public void setIndex(int index) {
    this.index = index;
}
public String getX() {
    return x;
}
public void setX(String x) {
    this.x = x;
}
public String getY() {
    return y;
}
public void setY(String y) {
    this.y = y;
}
public String getWkt() {
    return wkt;
}
public void setWkt(String wkt) {
    this.wkt = wkt;
}
  
}
