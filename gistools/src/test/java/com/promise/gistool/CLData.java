public class CLData {
    public String ring;
    public String startName;
    public String endName;
    public String ringType;
    public String code;
    public String pointWKT;
    public String lineWKT;
    
    public String getRing() {
        return ring;
    }
    public void setRing(String ring) {
        this.ring = ring;
    }
    public String getStartName() {
        return startName;
    }
    public void setStartName(String startName) {
        this.startName = startName;
    }
    public String getEndName() {
        return endName;
    }
    public void setEndName(String endName) {
        this.endName = endName;
    }
    public String getRingType() {
        return ringType;
    }
    public void setRingType(String ringType) {
        this.ringType = ringType;
    }
    public String getCode() {
        return code;
    }
    public void setCode(String code) {
        this.code = code;
    }
    public String getPointWKT() {
        return pointWKT;
    }
    public void setPointWKT(String pointWKT) {
        this.pointWKT = pointWKT;
    }
    public String getLineWKT() {
        return lineWKT;
    }
    public void setLineWKT(String lineWKT) {
        this.lineWKT = lineWKT;
    }
    
}
